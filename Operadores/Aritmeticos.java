package Operadores;

public class Aritmeticos {

	public static void main(String[] args) {
		
		System.out.println(2 + 3);
		
		var x = 34.56;
		double y = 2.44;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		var m = 5;
		int n = 12;
		
		System.out.println(m + n);
		System.out.println(m - n);
		System.out.println(m * n);
		System.out.println(m / n);
		System.out.println(m / (double) n);
		
		System.out.println(8 % 3);
		System.out.println(m % n);
		
	}
}
